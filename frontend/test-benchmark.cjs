const chunks = [];
const numChunks = 20000;
const chunkStr = "This is a sentence that is generated by the AI in chunks. ";

console.log("Starting benchmark for documentChunks.join('')");
let totalJoinTime = 0;
for (let i = 0; i < numChunks; i++) {
  chunks.push(chunkStr);
  const start = performance.now();
  const content = chunks.join('');
  const end = performance.now();
  totalJoinTime += (end - start);
}
console.log(`Time spent joining strings across ${numChunks} chunks: ${totalJoinTime}ms`);

console.log("Starting benchmark for node iteration in useStore");
let nodes = [];
let totalNodeMapTime = 0;
for (let i = 0; i < 5000; i++) {
  nodes.push({ id: `node_${i}`, type: 'agent', status: 'active' });
  const start = performance.now();
  const newNodes = nodes.map(n => (n.type === 'agent' && n.id !== `node_${i}`) ? { ...n, status: 'completed' } : n);
  nodes = newNodes;
  const end = performance.now();
  totalNodeMapTime += (end - start);
}
console.log(`Time spent mapping nodes across 5000 iterations: ${totalNodeMapTime}ms`);
